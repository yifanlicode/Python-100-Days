a = 321
b = 123

print(a + b)  #addition
print(a - b)  #Subtraction
print(a * b)  #Multiplication
print(a / b)  #Division
print(a // b)  #round the result down to the nearest integer.
print(a % b)  #returns the remainder of the division of a by b.
print(a ** b) # a的b次幂 raises a to the power of b.
