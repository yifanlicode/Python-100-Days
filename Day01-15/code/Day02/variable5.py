a = 100
b = str(a)
c = 12.345
d = str(c)
e = '123'
f = int(e)
g = '123.456'
h = float(g)
i = False
j = str(i)
k = 'hello'
m = bool(k)
print(a)  # 100
print(type(a))  # int
print(b)  # 100
print(type(b))  # str
print(c)  # 12.345
print(type(c))  # float
print(d)  # 12.345
print(type(d))  # str
print(e)  # 123
print(type(e))  # str
print(f)  # 123
print(type(f))  # int
print(g)  # 123.456
print(type(g))  # str
print(h)  # 123.456
print(type(h))  # float
print(i)  # false
print(type(i))  # bool
print(j)  # false
print(type(j))  # str
print(k)  # hello
print(type(k))  # str
print(m)  # true
print(type(m))  # bool
