a = 100
b = 1000000000000000000
c = 12.345
d = 1 + 5j
e = 'A'
f = 'hello, world'
g = True
print(type(a)) #<class 'int'>
print(type(b)) #<class 'int'>
print(type(c)) #<class 'float'>
print(type(d)) #<class 'complex'>
print(type(e)) #<class 'str'>
print(type(f))  #<class 'str'>
print(type(g)) #<class 'bool'>
